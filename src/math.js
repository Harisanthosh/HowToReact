export function add(a, b) {
    return a + b;
}

export function sub(a, b) {
    return a - b;
}

export function mul(a, b) {
    return a * b;
}

export function div(a, b) {
    return a / b;
}

export function mod(a, b) {
    return a % b;
}
export const PI = 3.14;

//Multiple named exports exports everything as an object, { add : function add(a, b), sub :function sub(a,b) }

